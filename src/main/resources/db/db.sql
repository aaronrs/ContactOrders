CREATE SEQUENCE CONTACT_SEQ;
CREATE TABLE contacts( id INT GENERATED BY DEFAULT AS SEQUENCE CONTACT_SEQ PRIMARY KEY, first VARCHAR(25), last VARCHAR(50), number INT, houseName VARCHAR(20), address1 VARCHAR(60), town VARCHAR(30), county VARCHAR(20), postcode VARCHAR(10), telephone VARCHAR(15) );
CREATE SEQUENCE ORDER_SEQ;
CREATE TABLE orders( id INT GENERATED BY DEFAULT AS SEQUENCE ORDER_SEQ PRIMARY KEY, contactId INT, productId INT, year INT, month INT, day INT, amount INT );
CREATE TABLE products( id INT PRIMARY KEY, categoryId INT, name VARCHAR(25), description VARCHAR(50) );
CREATE TABLE todos( id INT PRIMARY KEY, contactId INT, start DATE, end DATE, notes VARCHAR(500) );
